package com.letchlab.training;

public class DataTypes
{
    /**
     * Practicing data types
     * @param args
     */
    public static void main(String[] args) {

        byte byteNumber = 100;
        System.out.println(byteNumber);
        short shortNumber = 32767;
        System.out.println(shortNumber);
        int intNumber = 8787687;
        System.out.println(intNumber);
        long longNum = 5646498769879898799L;
        System.out.println(longNum);
        float floatNum = 10.231235687685687868f;
        System.out.println(floatNum);
        double doublenum = 10.231235687685687868d;
        System.out.println(doublenum);
        boolean isOk = true;
        System.out.println(isOk);
        char charvalue = 'a';
        System.out.println(charvalue);
        char asca = 77;
        System.out.println(asca);

    }
}
